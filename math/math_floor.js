const floor_v1 = Math.floor(5.95); // 5
const floor_v2 = Math.floor(5.05); // 5
const floor_v3 = Math.floor(5);    // 5
const floor_v4 = Math.floor(-5.95); //-6
const floor_v5 = Math.floor(-5.05); // -6

console.log('floor_v1 : ', floor_v1);
console.log('floor_v2 : ', floor_v2);
console.log('floor_v3 : ', floor_v3);
console.log('floor_v4 : ', floor_v4);
console.log('floor_v5 : ', floor_v5);